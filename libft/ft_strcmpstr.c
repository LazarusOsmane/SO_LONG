#include "libft.h"
#include <stdio.h>

int ft_strcmpstr(char *s1, char *s2, int *c)
{
    if (!s1)
        return (0);
    while (*s1)
    {
        if (c && (*s1 == 'P' || *s1 == 'E' || *s1 == 'C'))
            *c = *c + 1;
        if (!ft_strchr(s2, *s1++))
            return (0);
    }
    return (1);
}
