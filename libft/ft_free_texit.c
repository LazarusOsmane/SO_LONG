#include "libft.h"

void    ft_free_texit(void **s, char *err)
{
    int i;

    i = -1;
    while (s && s[++i])
        free(s[i]);
    if (s)
        free(s);
    ft_putstr_fd(err, 0);
    exit(0);
}

